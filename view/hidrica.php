            <p align="center" class="greenfont1">
                PROGRAMA DE SEGURIDAD HÍDRICA<br>
                <small><small>BAWÍ RARÁMURI-"AGUA PARA LA TARAHUMARA"</small></small>
            </p>
        </div>
        <p align="justify">
            El programa Bawí Rarámuri fue creado en 2006 para aumentar la disponibilidad de agua para consumo humano y agrícola en comunidades de la Sierra Tarahumara. Este programa busca asegurar la conservación y el manejo sustentable de los recursos renovables de comunidades de la Sierra Tarahumara a través de acciones de ordenamiento territorial, captación y siembra de agua, conservación-recuperación de suelos, y almacenamiento y distribución de agua. Actualmente, el programa opera en cuatro municipios de la Sierra Tarahumara: Bocoyna, Carichí, Guachochi, y Guadalupe y Calvo.
        </p><br><br>
        <div style="display:flex;justify-content:center; align-items:center; flex-wrap:wrap;">

            <img src="source/images/programas/prog-seghid30.gif"  alt="" style="padding:1rem;" class="col-md-3">
            <p align="justify" class="col-md-9"><strong class="greenfon3">Impactos: </strong>La conservación de agua y suelo, y posteriormente el almacenamiento y distribución de agua, son estrategias eficaces para fomentar la seguridad alimentaria, aumentando la disponibilidad de agua para consumo humano y la producción agrícola de autoconsumo, y así revertiendo ciertas causas estructurales de la malnutrición, pobreza y marginación. 
            <br></p><br>
        </div>
        <div class="col-md-2"></div>
        <div align="center" class="col-md-8 greenfon2"><br>
            En el 2012, el programa fue finalista del premio Latinoamericano “Acciones en Agua y Adaptación” en el VI Foro Mundial del Agua en Marsella, Francia.
        </div>
    </div>
    <div class="row">
        <div class="col-md-12">
            <br><br>
            <p align="center" class="greenfon2">
                <span class="greenfont1">COMPONENTES DEL PROGRAMA</span>          
            </p>
            <br><br>
        </div>
        <div class="col-md-12" style="display:flex;justify-content:center;align-items:center;margin-bottom:2rem;flex-wrap:wrap;">
            <div class="col-md-2"><img src="source/images/programas/prog-seghid24.gif"  alt="" style="margin:1rem;margin-right:3rem;"></div>
            <div class="col-md-10">
                Conservación: construcción de obras para la recarga de mantos acuíferos, retención de suelos, filtración y almacenamiento de agua. Las obras que se emplean son:<br>
                a. represas de mampostería<br>
                b. represas filtrantes de piedra acomodada<br>
                c. barreras de piedra acomodada en curvas de nivel<br>
                d. represas de gaviones<br>
            </div>
        </div>
        <div class="col-md-12" style="display:flex;justify-content:center;align-items:center;margin-bottom:2rem;flex-wrap:wrap;">
            <div class="col-md-2"><img src="source/images/programas/prog-seghid25.gif"  alt="" style="margin:1rem;margin-right:3rem;"></div>
            <div class="col-md-10">
                Infraestructura: la construcción de sistemas de captación como son las pilas de almacenamiento y sistemas de distribución de agua para parcelas y hogares.
            </div>
        </div>
        <div class="col-md-12" style="display:flex;justify-content:center;align-items:center;margin-bottom:2rem;flex-wrap:wrap;">
            <div class="col-md-2"><img src="source/images/programas/prog-seghid29.gif"  alt="" style="margin:1rem;margin-right:3rem;"></div>
            <div class="col-md-10">
                Manejo integral de parcelas: construcción de barreras para la nivelación de parcelas y el mejoramiento de la fertilidad de los suelos a partir de la siembra de cultivos de cobertura (siembra de veza).
            </div>
        </div>
        <div class="col-md-12" style="display:flex;justify-content:center;align-items:center;margin-bottom:2rem;flex-wrap:wrap;">
            <div class="col-md-2"><img src="source/images/programas/prog-seghid26.gif"  alt="" style="margin-right:3rem;margin:1rem;"></div>
            <div class="col-md-10">
                Organización e Inversión Comunitaria: Las comunidades participan en el diseño, ejecución y evaluación de las obras y establecen comités comunitarios como estrategia de organización. Cada comunidad designa a una persona como maestro de obras, quien recibe capacitaciones continuas y supervisa la construcción de las obras en conjunto con los técnicos de la Fundación. En el esquema de inversión comunitaria, las comunidades aportan el material local, recolectando la piedra y la arcilla para la construcción de las obras.
            </div>
        </div>
        <div class="col-md-12" style="display:flex;justify-content:center;align-items:center;margin-bottom:2rem;flex-wrap:wrap;">
            <div class="col-md-2"><img src="source/images/programas/prog-seghid27.gif"  alt="" style="margin-right:3rem;margin:1rem;"></div>
            <div class="col-md-10">
                Fortalecimiento del capital social local: La Fundación imparte talleres para fortalecer las capacidades técnicas y organizativas de las comunidades. Durante al año, los participantes asisten a capacitaciones continuas en temas como el Manejo Integral de Cuencas y la construcción de obras.
            </div>
        </div>
        <div class="col-md-12" style="display:flex;justify-content:center;align-items:center;margin-bottom:2rem;flex-wrap:wrap;">
            <div class="col-md-2"><img src="source/images/programas/prog-seghid28.gif"  alt="" style="margin-right:3rem;margin:1rem;"></div>
            <div class="col-md-10">
                Generación de empleo temporal local: La Fundación ofrece un apoyo económico a las personas de las comunidades que participan en la construcción de las obras. Este apoyo fortalece la economía local de las familias participantes y reduce la migración.
            </div>
        </div>
        <div class="col-md-12">
            <p align="justify"><br><br>
                Logros Históricos: De 2007 a 2011 se han construido 30 proyectos hidro-agroecológicos con 786 obras que beneficiaron directamente a 1,919 habitantes. En el 2012, el programa contó con 1,754 beneficiarios con 22 proyectos con la construcción de 873 obras que equivalen a 7,997 metros cúbicos de construcción y 23,900 metros lineales de sistemas de distribución. En el 2013, se están llevando a cabo 20 proyectos. En 2014 se hicieron 42 obras de conservación, almacenamiento y distribución de agua, se construyeron 23 pilas de almacenamiento equivalentes a 401,785 litros de agua almacenada.<br><br>
                Trabajamos con 19 comuninades de la Sierra involucrando a mas de 2,400 personas y con su ayuda concluímos 19 obras de conservación de suelo y agua.<br><br>
                En 2015 se capacitaron a 146 personas en el uso del purificador FILTREOZ a partir de 5 talleres que se impartieron, uno por localidad (Casa Blanca, Santa Cruz, Tucheachi de Norogachi, Las Turbinas y Simuchichi. Las personas que participaron en los talleres se capacitaron en temas de armado del FiltrEOZ, demostración completa del sistema de purificación de agua FiltrEOZ, funcionamiento y mantenimiento correcto del filtro. <a href="http://www.agualimpia.mx/pages/proyectos">http://www.agualimpia.mx/pages/proyectos</a>
            
