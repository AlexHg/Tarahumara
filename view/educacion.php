            <p align="center" class="greenfon2">
                <span class="greenfont1">COMPONENTES DEL PROGRAMA</span>          
            </p>
            <br><br>
        </div>
        <div class="col-md-12" style="display:flex;justify-content:center;align-items:center;margin-bottom:2rem;flex-wrap:wrap;">
            <div class="col-md-2"><img src="source/images/programas/prog-educa6.gif"  alt="" style="margin:1rem;margin-right:3rem;"></div>
            <div class="col-md-10">
                <strong class="greenfon3">Apoyos económicos mensuales: </strong>
                Los apoyos mensuales permiten que estudiantes indígenas y mestizos continúen sus estudios de secundaria, preparatoria y universidad. Debido a la falta de servicios educativos en la Sierra Tarahumara los estudiantes deben salir de sus comunidades de origen y vivir en poblados donde existan escuelas y universidades. Esto se logra gracias a la entrega de apoyos económicos con los que cubren parte de los costos de manutención, hospedaje, transporte, útiles escolares y en algunos casos colegiatura.
            </div>
        </div>
        <div class="col-md-12" style="display:flex;justify-content:center;align-items:center;margin-bottom:2rem;flex-wrap:wrap;">
            <div class="col-md-2"><img src="source/images/programas/prog-educa7.gif"  alt="" style="margin:1rem;margin-right:3rem;"></div>
            <div class="col-md-10">
                <strong class="greenfon3">Programa de tutoría:</strong>
                Red de maestros voluntarios en cada escuela que apoyan a cada estudiante para que continúe sus estudios, y que tenga un mejor rendimiento académico.
            </div>
        </div>
        <div class="col-md-12" style="display:flex;justify-content:center;align-items:center;margin-bottom:2rem;flex-wrap:wrap;">
            <div class="col-md-2"><img src="source/images/programas/prog-educa8.gif"  alt="" style="margin:1rem;margin-right:3rem;"></div>
            <div class="col-md-10">
                <strong class="greenfon3">Reuniones mensuales:</strong>
                Los coordinadores regionales de la Fundación ofrecen capacitaciones continuas y acompañamiento cercano a los estudiantes para regularizar el nivel académico y fortalecer conocimientos que no se consideran en la educación formal (ej. lengua y cultura).
            </div>
        </div>
        <div class="col-md-12" style="display:flex;justify-content:center;align-items:center;margin-bottom:2rem;flex-wrap:wrap;">
            <div class="col-md-2"><img src="source/images/programas/prog-educa9.gif"  alt="" style="margin-right:3rem;margin:1rem;"></div>
            <div class="col-md-10">
                <strong class="greenfon3">Encuentro Estudiantil:</strong>
                Cada año los estudiantes se reúnen para recibir capacitaciones en temas de educación y desarrollo comunitario, participar en actividades culturales y conocer a estudiantes de otras regiones. Los encuentros crean lazos de identidad cultural y crean un sentido de unión entre los estudiantes apoyados por la Fundación. Estos lazos ayudan a disminuir los índices de deserción escolar.
            </div>
        </div>
        <div class="col-md-12" style="display:flex;justify-content:center;align-items:center;margin-bottom:2rem;flex-wrap:wrap;">
            <div class="col-md-2"><img src="source/images/programas/prog-educa10.gif"  alt="" style="margin:1rem;margin-right:3rem;"></div>
            <div class="col-md-10">
                <strong class="greenfon3">Servicio Comunitario “Norina Bitichí” (vuelve a casa): </strong>
                Nuestros estudiantes realizan su servicio comunitario en las comunidades participando en proyectos de la Fundación, como son la construcción de obras de conservación de agua y suelo, instalación de huertos, implementación de diagnósticos, etc. La participación de los jóvenes en proyectos de desarrollo los sensibiliza ante las necesidades de su propia comunidad, generando así una vinculación con la misma y creando un sentido de corresponsabilidad y compromiso con su comunidad de origen.Los estudiantes de preparatoria y universidad realizan su servicio durante el mes de julio y se hospedan en la comunidad donde trabajan. Los estudiantes de secundaria realizan su servicio durante el ciclo escolar.
