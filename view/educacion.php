                Lograr que adolescentes y jóvenes de la Sierra Tarahumara completen su secundaria, preparatoria y universidad.<br>
                Ayudar a que los jóvenes egresados se conviertan en líderes y agentes de cambio.<br>
                Impulsar la autonomía de los adolescentes y jóvenes, sus familias y comunidades de la Sierra Tarahumara mediante esquemas de educación formal e informal y dentro de una perspectiva intercultural.
