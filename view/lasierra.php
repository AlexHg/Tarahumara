            <p >La Sierra Tarahumara, ubicada al suroeste del estado de Chihuahua, es una región agreste de 65 mil kilómetros cuadrados, conformada por una cadena de montañas y barrancas cuya altitud varía de los 0 a 2,800 metros sobre el nivel del mar, y en donde se dan climas extremos que alcanzan hasta 40oC durante el verano en las barrancas y hasta -20oC en las regiones más altas durante el invierno.</p>
